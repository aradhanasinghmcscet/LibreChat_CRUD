const mongoose = require('mongoose');
const { logger } = require('@librechat/data-schemas');

const todoSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
    trim: true,
    maxlength: 100
  },
  description: {
    type: String,
    trim: true,
    maxlength: 500
  },
  status: {
    type: String,
    enum: ['pending', 'in-progress', 'completed'],
    default: 'pending'
  },
  createdAt: {
    type: Date,
    default: Date.now
  },
  updatedAt: {
    type: Date,
    default: Date.now
  }
}, {
  timestamps: true
});

// Add indexes
todoSchema.index({ status: 1 });

const Todo = mongoose.model('Todo', todoSchema);

module.exports = {
  Todo,
  todoSchema
};
